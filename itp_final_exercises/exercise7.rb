# What's the major difference between an Array and a Hash

# While both arrays and hashes are data structures they store information very differently.

# Arrays store a list of data. 
# Array example:
 [1, 2, 3, 4, 5]

# Hashes store data based on key value pairs and can be referenced by key. 
# Hash example
 {cow: "moo", dog: "roof"}