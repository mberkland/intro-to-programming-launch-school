# Can hash values be array? Can you have an array of hashes? (give examples)

# Yes, hash values can be arrays.
# Example
friends = { college: ["Grace", "Rachel", "Steven", "Tommy", "Ben"],
            hometown: ["Hannah", "Josh", "Jonathan"],
            neighbors: ["Peter", "Mandy", "Mary"],
            work: ["Carly", "Russel", "July", "James"]
} 

